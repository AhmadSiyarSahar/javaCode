package com.class04;

public class BooleanExample {

	public static void main(String[] args) {
		// Declare Boolean variable isRaining, and if is true===> stay home

		boolean isRaining = true;
		if (isRaining) {
			System.out.println("Stay home and code");
		} else {
			System.out.println("Take a walk and listen Java");
		}
		// Second way if is not raining then;

		boolean isRaining1 = false;
		if (isRaining1) {
			System.out.println("Stay home and code");
		} else {
			System.out.println("Take a walk and listen Java");
		}

	}

}
