package com.class03;

public class PrintingInSameLine {

	public static void main(String[] args) {
		// printing in same line:

		String str = "Hello";
		String str1 = "Bye";
		System.out.print(str); // it prints the whole program in the same line.
		System.out.println(str + str1); // it prints only line by line from top to bottom.

	}

}
